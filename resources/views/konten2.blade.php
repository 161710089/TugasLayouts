15.Djajang Nurjaman<br>
<p>Sebagai pelatih, Djadjang merasakan gelar juara ketika menjadi asisten pelatih Indra M. Thohir di Liga Indonesia (LI) I/1994-1995 dan masih dipercaya hingga tahun 1996. Setelah itu ia lebih memantapkan karier kepelatihan dengan menukangi PERSIB Junior (U-23). Pada tahun 2006 lagi-lagi ia mendapat kepercayaan sebagai asisten pelatih untuk mendampingi Arcan Iurie. Setelah itu ia mengembangkan karier kepelatihan di luar PERSIB, hingga pada tahun 2012, manajemen PERSIB mempercayakan dirinya untuk menukangi tim sebagai Pelatih Kepala dalam mengarungi Indonesia Super League tahun 2013.</p>

<p>Kembalinya ke PERSIB seolah mengulang romantisme juara dengan rekan-rekannya di Liga Indonesia I. Namun kali ini ia menjadi pelatih kepala, "abah" Indra Thohir sebagai Direktur Teknik, dan juga ia dibantu oleh trio mantan pemain yang mengantarkan PERSIB juara LI I, yaitu Anwar Sanusi, Asep Soemantri, dan Sutiono Lamso sebagai asisten pelatih. Hasilnya tidak mengecewakan, di ajang turnamen pra musim Celebes Cup yang digelar di kota Bandung, Djadjang mempersembahkan tropi juara setelah di final mengalahkan Sriwijaya FC dengan skor 1-0.</p>

<p> Musim selanjutnya (2014) Djadjang masih didaulat sebagai pelatih kepala, kali ini ia mengajak Herrie Setiawan, Asep Soemantri dan Anwar Sanusi sebagai asisten pelatih.Pada Musim ini,Djadjang Nurdjaman berhasil membawa Persib menjadi juara isl 2014.Djadjang juga mencetak sebuah rekor,yaitu mengantarkan Persib menjadi juara liga sebagai pemain,asisten pelatih, dan pelatih kepala</p>

<p>Musim 2015 Djajang menjalani musim ketiga bersama Persib. Di QNBL 2015 Djajang berhasil meraih 2 kemenangan dari 2 pertandingan sebelum liga dihentikan karena PSSI dibekukan oleh FIFA. Di kompetisi AFC Cup Djajang berhasil membawa Persib lolos ke babak 16 besar,Di AFC cup langkah persib terhenti di 16 besar karena kalah 0-2 dari Hanoi T&T .</p>

<p>Satu tropi kembali Djajang sumbangkan untuk Persib pada tahun 2015. Persib dipimpinnya menjadi juara Piala Presiden 2015. setelah 20 tahun akhirnya Djajang kembali ke Stadion GBK, sebagai pelatih Djajang berhasil meraih gelar Juara mengalahkan Sriwijaya FC 2-0.</p>

<p>Akhirnya, pada tanggal 15 Juli 2017 Djajang resmi mengundurkan diri sebagai pelatih kepala Persib Bandung dikarena kecewa tidak bisa mendongkrak penampilan tim berjuluk "Maung Bandung" hingga menyelesaikan laga ke-15 kompetisi Liga 1 2017.</p> 


